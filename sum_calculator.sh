#!/usr/bin/env bash

# Скрипт запрашивает два числа и выводит их сумму

read -rp "Введите первое число: " a
read -rp "Введите второе число: " b

# Проверка, что введены числа
if ! [[ "$a" =~ ^-?[0-9]+([.][0-9]+)?$ ]] || ! [[ "$b" =~ ^-?[0-9]+([.][0-9]+)?$ ]]; then
  echo "Ошибка: нужно вводить числа."
  exit 1
fi

# Если оба целые — используем арифметику Bash, иначе bc
if [[ "$a" =~ ^-?[0-9]+$ ]] && [[ "$b" =~ ^-?[0-9]+$ ]]; then
  sum=$((a + b))
else
  if ! command -v bc >/dev/null 2>&1; then
    echo "Для работы с вещественными числами требуется утилита bc."
    exit 1
  fi
  sum=$(echo "$a + $b" | bc)
fi

echo "Сумма: $sum"


