#!/usr/bin/env bash

# Скрипт определяет, является ли число чётным или нечётным

read -rp "Введите целое число: " num

if ! [[ "$num" =~ ^-?[0-9]+$ ]]; then
  echo "Ошибка: нужно ввести целое число."
  exit 1
fi

if (( num % 2 == 0 )); then
  echo "Число $num является чётным."
else
  echo "Число $num является нечётным."
fi


